    /** id капстоуна по профессии (для клавиши G и HUD). */
    public static Node capstone(int profId) { return capstone(profId, 4); }

    /** Активка ряда tier (4 или 7) выбранной профессии. */
    public static Node capstone(int profId, int tier) {
        for (Node n : ALL) if (n.profId() == profId && n.tier() == tier) return n;
        return null;
    }

    public static List<Node> forProfession(int profId) {
        List<Node> out = new ArrayList<>();
        for (Node n : ALL) if (n.profId() == profId) out.add(n);
        return out;
    }

    /** Минимальный уровень профессии для ряда — зеркало TalentCatalog.tierGate. */
    public static int tierGate(int tier) {
        return switch (tier) {
            case 1 -> 1;
            case 2 -> 10;
            case 3 -> 25;
            case 4 -> 40;
            case 5 -> 50;
            case 6 -> 75;
            default -> 100; // ряд 7 — вторая активка
        };
    }
